//实现加法运算
const add = (num1, num2) => {
    return num1 + num2;
};

//实现减法运算
const subtract = (num1, num2) => {
    return num1 - num2;
};

//实现乘法运算
const multiply = (num1, num2) => {
    return num1 * num2;
};

//实现除法运算fllename     diM
const divide = (num1, num2) => {
    return num1 / num2;
};